export function millimetersToMeters(value: number): number {
  return value / 1000;
}

export function metersToMillimeters(value: number): number {
  return value * 1000;
}

export function pascalsToKilopascals(value: number): number {
  return value / 1000;
}

export function cubicMetersPerSecondToLitersPerSecond(value: number): number {
  return value * 1000;
}
